#include "math.h"

int	ft_sqrt(int n)
{
	int	i;

	if (n < 0)
		return (-1);
	i = 0;
	while (++i < n)
		n -= i++;
	if (i != n)
		return (-1);
	return (i / 2 + 1);
}
